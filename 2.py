#Antes de empezar vamos a poner un texto que nos funcione para todos los ejemplos que voy a poner
a="pedro Felipe Gómez"
#La primera función que quiero mostrar es "swapcase()" el cual permite invertir el case (mayúsculas a minúsculas y viceversa) todas las palabras 
#se utiliza mediante declarandolo en una variable e imprimiéndola. Esto aplica para todas las funciones
c=a.swapcase()
print(c)
#La segunda funcion a utilizar es "isdecimal()" el cual me dice si lo ingresado es decimal o no mediante True o False
d=a.isdecimal()
print(d)
#La tercera función a utilizar es "upper()" el cual convierte todo lo que está en minúscula en mayúscula
e=a.upper()
print(e)
#La cuarta función a utilizar es "isnumeric()" el cual se encarga de decirme si un string tiene solamente carácteres numéricos mediante True o False
f=a.isnumeric()
