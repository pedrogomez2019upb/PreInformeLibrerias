#La quinta función es "capitalize()" la cual capitaliza la primera letra de un string
g=a.capitalize()
print(g)
#La sexta función es "isspace()" el cual me dice si el string solamente contiene carácteres con espacio
h=a.isspace()
print(h)
#La séptima función es "lower()" el cual se encarga de convertir todo a minúscula
j=a.lower()
print(j)
#La octava función se llama "istitle()" que sirve para saber si un string tiene las propiedades de un título
k=a.istitle()
print(k)
#La novena función es "isupper()" la cual dice si todos los carácteres son mayúsculas
m=a.isupper()
print(m)
#La novena función es "islower()" que sirve para decir si todos los carácteres estan en minúscula
n=a.islower() 
