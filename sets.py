#Sets
#Se crea un set para el ejemplo
mi_set={23,34,56}
#Primera función
print(mi_set)
#Segunda función
mi_set.add(2)
print(mi_set)
#Tercera función
mi_set.update([4,5],{43,56})
print(mi_set)
#Cuarta función
mi_set.discard(23)
print(mi_set)
#Quinta función
mi_set.remove(34)
print(mi_set)
